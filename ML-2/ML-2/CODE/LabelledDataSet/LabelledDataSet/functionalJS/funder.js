function funder( funder, oorsprong ) {

	var url;
	var id;
	
	if ( typeof( funders[funder] ) == "undefined") {
		return;
	}
	
	if ( typeof( oorsprong ) == "undefined") {
		oorsprong = "";
	}

  id = funder;

/* EIGENLIJK vind ik dat de funder-code zonder logo_ zou moeten zijn.
   Maar ja.
   
  if ( id.substring( 0, 5 ) == "logo_" ) {
    id = id.substring( 5 );
  }
*/
	if (typeof(funders[funder]["url"])!="undefined")
	{
		url =  "/info/funder/redirect.php?funder=" + id;
		url += "&url=" + escape( funders[funder]["url"] );
		if ( oorsprong != "" ) {
			url += "&oorsprong=" + oorsprong;
		}

		window.open( url );
	}
	return;
}
